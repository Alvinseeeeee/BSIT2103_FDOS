    private void ConfirmSelectionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmSelectionButtonActionPerformed
        try {
        Connection conn = DBConnect.getConnection();

        int customerId = SharedData.customerID;
        Integer reservationId = SharedData.reservationID;

        if (customerId == 0) {
            JOptionPane.showMessageDialog(this, "Customer ID is not valid.");
            return;
        }
        
        //new
        String customerFullName = "";
        String customerQuery = "SELECT customer_fullname FROM customer WHERE customer_id = ?";
        PreparedStatement psCustomer = conn.prepareStatement(customerQuery);
        psCustomer.setInt(1, customerId);
        ResultSet rsCustomer = psCustomer.executeQuery();

        if (rsCustomer.next()) {
            customerFullName = rsCustomer.getString("customer_fullname");
        } else {
            JOptionPane.showMessageDialog(this, "Customer not found in the database.");
            return;
        }//new
        
        //new
        String tableId = "";
        String waiterName = "";

        String tableWaiterQuery = """
        SELECT t.table_id, w.waiter_name 
        FROM tables t
        JOIN waiters w ON t.waiter_id = w.waiter_id
        JOIN reservationdetails r ON t.table_id = r.table_id
        WHERE r.reservation_id = ?
        """;

        PreparedStatement psTableWaiter = conn.prepareStatement(tableWaiterQuery);
        psTableWaiter.setInt(1, reservationId);
        ResultSet rsTableWaiter = psTableWaiter.executeQuery();

        if (rsTableWaiter.next()) {
            tableId = rsTableWaiter.getString("table_id");
            waiterName = rsTableWaiter.getString("waiter_name");
        } else {
            JOptionPane.showMessageDialog(this, "Table or waiter information not found for the reservation.");
            return;
        }
        //new
        
        updateFields();

        double amountPaid = Double.parseDouble(AmountField.getText());

        // Validate payment
        if (amountPaid < total) {
            JOptionPane.showMessageDialog(this, "Amount paid is less than the total price. Please enter a valid amount.");
            return;
        }

        // Handle conditional payment details
        String chequeNumber = null;
        String cardNumber = null;

        if (paymentMethod.equals("Cheque")) {
            chequeNumber = chequeNumberField.getText().trim();
            if (chequeNumber.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a valid cheque number.");
                return;
            }
        } else if (paymentMethod.equals("Credit Card")) {
            cardNumber = CardNumberField.getText().trim();
            if (cardNumber.isEmpty() || cardNumber.length() != 16) {
                JOptionPane.showMessageDialog(this, "Please enter a valid 16-digit card number.");
                return;
            }
        }

        double change = amountPaid - total;

        String insertOrderQuery = "INSERT INTO orders (customer_id, order_date, reservation_id, subtotal, tip, total_price, order_placed) VALUES (?, NOW(), ?, ?, ?, ?, TRUE)";
        PreparedStatement psOrder = conn.prepareStatement(insertOrderQuery, Statement.RETURN_GENERATED_KEYS);
        psOrder.setInt(1, customerId);
        if (reservationId != null) {
            psOrder.setInt(2, reservationId); 
        } else {
            psOrder.setNull(2, Types.INTEGER);
        }
        psOrder.setDouble(3, subtotal);
        psOrder.setDouble(4, tip);
        psOrder.setDouble(5, total);
        psOrder.executeUpdate();

        ResultSet rsOrder = psOrder.getGeneratedKeys();
        rsOrder.next();
        int orderId = rsOrder.getInt(1); 
        
        String insertOrderItemsQuery = "INSERT INTO order_items (order_id, menu_id, quantity, total_price) VALUES (?, ?, ?, ?)";
        PreparedStatement psItems = conn.prepareStatement(insertOrderItemsQuery);
        StringBuilder receiptContent = new StringBuilder(); // To hold receipt details
        double itemSubtotal = 0;
        
        receiptContent.append("**********************************************************************\n")
                  .append("                              Baggy's Diner\n")
                  .append("**********************************************************************\n")
                  .append("Customer: ").append(customerFullName).append("\n")
                  .append("Order Date: ").append(new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new java.util.Date())).append("\n");

        if (!tableId.isEmpty()) {
            receiptContent.append("Table ID: ").append(tableId).append("\n");
        }

        if (!waiterName.isEmpty()) {
            receiptContent.append("Waiter: ").append(waiterName).append("\n");
        }

        receiptContent.append("-------------------------------------------------------------------------------------------\n")
                .append("                              Thank You!\n");

        for (OrderItem item : receiptItems) {
            psItems.setInt(1, orderId);
            psItems.setInt(2, item.getMenuId());
            psItems.setInt(3, item.getQuantity());
            psItems.setDouble(4, item.getTotalPrice());
            psItems.addBatch();
            
            receiptContent.append(item.getQuantity())
                      .append(" x ")
                      .append(item.getItemName())
                      .append(" - PHP ")
                      .append(String.format("%.2f", item.getTotalPrice()))
                      .append("\n");
            itemSubtotal += item.getTotalPrice();
        }
        psItems.executeBatch();

        String insertPaymentQuery = "INSERT INTO payments (order_id, payment_method, amount_paid, cheque_number, card_number, payment_date) VALUES (?, ?, ?, ?, ?, NOW())";
        PreparedStatement psPayment = conn.prepareStatement(insertPaymentQuery);
        psPayment.setInt(1, orderId);
        psPayment.setString(2, paymentMethod);
        psPayment.setDouble(3, amountPaid);
        psPayment.setString(4, chequeNumber);
        psPayment.setString(5, cardNumber);
        psPayment.executeUpdate();

        receiptContent.append("\nSubtotal: PHP " + String.format("%.2f", subtotal))
                  .append("\nTip: PHP " + String.format("%.2f", tip))
                  .append("\nTotal: PHP " + String.format("%.2f", total))
                  .append("\nAmount Paid: PHP " + String.format("%.2f", amountPaid))
                  .append("\nChange: PHP " + String.format("%.2f", change));

        ReceiptTextArea.setText(receiptContent.toString());

        JOptionPane.showMessageDialog(this, "Order confirmed!\nChange: PHP " + String.format("%.2f", change));
        //new
        MainPageForm main = new MainPageForm();
        main.setVisible(true);
        this.dispose();
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error confirming order!");
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter a valid amount for payment.");
    }
    }//GEN-LAST:event_ConfirmSelectionButtonActionPerformed

    private double calculateSubtotal() {
        double subtotal = 0;
        for (OrderItem item : receiptItems) {
            subtotal += item.getTotalPrice();
        }
        return subtotal;
    }
    
    private double calculateTip(double subtotal) {
        return subtotal * 0.15;
    }
    
    private double calculateTotal(double subtotal, double tip) {
        return subtotal + tip;
    }
    
